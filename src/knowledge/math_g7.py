# 汇总与查询函数
# ──────────────────────────────────────────────

ALL_POINTS: list[KnowledgePoint] = GRADE_7_POINTS

# 索引缓存
_ID_MAP: dict[str, KnowledgePoint] = {p.id: p for p in ALL_POINTS}


def get_point_by_id(point_id: str) -> Optional[KnowledgePoint]:
    """根据知识点ID查询知识点，未找到返回None。"""
    return _ID_MAP.get(point_id)


def get_points_by_grade(grade: int = 7) -> list[KnowledgePoint]:
    """根据年级查询知识点列表。"""
    return [p for p in ALL_POINTS if p.grade == grade]


def get_points_by_difficulty(difficulty: Difficulty) -> list[KnowledgePoint]:
    """根据难度等级查询知识点列表。"""
    return [p for p in ALL_POINTS if p.difficulty == difficulty]
